package com.example.collegeportal;

public class QualificationDataClass {
    private String dataMain;
    private String dataDegree;
    private String dataExam;
    private String dataClass;
    private String dataBoard;
    private String dataSchool;
    private String dataYear;
    private String dataSeat;
    private String dataTotal;
    private String dataMarks;
    private String dataPercentage;
    private String dataState;
    private String dataCity;
    private String dataStudy;
    private String dataLanguage;

    public QualificationDataClass(String dataMain, String dataDegree, String dataExam, String dataClass, String dataBoard, String dataSchool, String dataYear, String dataSeat, String dataTotal, String dataMarks, String dataPercentage, String dataState, String dataCity, String dataStudy, String dataLanguage) {
        this.dataMain = dataMain;
        this.dataDegree = dataDegree;
        this.dataExam = dataExam;
        this.dataClass = dataClass;
        this.dataBoard = dataBoard;
        this.dataSchool = dataSchool;
        this.dataYear = dataYear;
        this.dataSeat = dataSeat;
        this.dataTotal = dataTotal;
        this.dataMarks = dataMarks;
        this.dataPercentage = dataPercentage;
        this.dataState = dataState;
        this.dataCity = dataCity;
        this.dataStudy = dataStudy;
        this.dataLanguage = dataLanguage;
    }

    public String getDataMain() {
        return dataMain;
    }

    public String getDataDegree() {
        return dataDegree;
    }

    public String getDataExam() {

        return dataExam;
    }

    public String getDataClass() {
        return dataClass;
    }

    public String getDataBoard() {
        return dataBoard;
    }

    public String getDataSchool() {
        return dataSchool;
    }

    public String getDataYear() {
        return dataYear;
    }

    public String getDataSeat() {
        return dataSeat;
    }

    public String getDataTotal() {
        return dataTotal;
    }

    public String getDataMarks() {
        return dataMarks;
    }

    public String getDataPercentage() {
        return dataPercentage;
    }

    public String getDataState() {
        return dataState;
    }

    public String getDataCity() {
        return dataCity;
    }

    public String getDataStudy() {
        return dataStudy;
    }

    public String getDataLanguage() {
        return dataLanguage;
    }
}